    public void affiche() {
        System.out.println(id);
        try {
            ResultSet rs = bd.read("personne").where("contactPers", "= ?").getResultSet(new String[]{id});
            if (rs.next()) {
                tel.setText(rs.getString("contactPers"));
                TNom.setText(rs.getString("nom"));
                TPrenom.setText(rs.getString("prenom"));
                TAdresse.setText(rs.getString("adresse"));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

