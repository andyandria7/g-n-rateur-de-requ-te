    private void modifierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modifierMouseClicked
        // TODO add your handling code here:

        String tele = tel.getText().trim();
        String name = TNom.getText().trim();
        String username = TPrenom.getText().trim();
        String mail = TAdresse.getText().trim();

        if (tel.getText().isEmpty() || TNom.getText().isEmpty() || TPrenom.getText().isEmpty() || TAdresse.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Remplir tout les champ !!! ");
            return;
        }

        bd.update("personne").set(new String[]{"contactPers", "nom", "prenom", "adresse"}).where("contactPers", "= ?").values(new String[]{tele, name, username, mail,id});
        JOptionPane.showMessageDialog(this, "Utilisateur Modifier");
        
       
        
        tel.setText("");
        TNom.setText("");
        TPrenom.setText("");
        TAdresse.setText("");
        
        p.afficher();
        this.dispose();

    }//GEN-LAST:event_modifierMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        // TODO add your handling code here:
        bd.delete("personne").where("contactPers", "= ?").values(new String[]{id});
        p.afficher();
        this.dispose();
    }//GEN-LAST:event_deleteMouseClicked

