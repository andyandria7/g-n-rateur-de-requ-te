    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
        // TODO add your handling code here:
        if (tel.getText().isEmpty() || TNom.getText().isEmpty() || TPrenom.getText().isEmpty() || TAdresse.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Remplir tout les champ !!! ");
            return;
        }
        this.bd.insert("personne")
                .fields(new String[]{"contactPers", "nom", "prenom", "adresse"})
                .values(new String[]{tel.getText(), TNom.getText(), TPrenom.getText(), TAdresse.getText()});

        JOptionPane.showMessageDialog(this, "Utilisateur Ajouter");

        tel.setText("");
        TNom.setText("");
        TPrenom.setText("");
        TAdresse.setText("");

        this.afficher();
    }//GEN-LAST:event_SaveMouseClicked

    private void tableauMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableauMouseClicked
        // TODO add your handling code here:
        int ligne = tableau.getSelectedRow();
        if (ligne >= 0) {
            selectedId = Integer.parseInt(tableau.getValueAt(ligne, 0).toString());
        }
    }//GEN-LAST:event_tableauMouseClicked

    private void infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoMouseClicked
        // TODO add your handling code here:
        int ligne = tableau.getSelectedRow();

        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans la table.");
            return;
        }
        String id = tableau.getValueAt(ligne, 0).toString();
        ModifPersonne mp = new ModifPersonne(id, this);
        mp.setVisible(true);

    }//GEN-LAST:event_infoMouseClicked

