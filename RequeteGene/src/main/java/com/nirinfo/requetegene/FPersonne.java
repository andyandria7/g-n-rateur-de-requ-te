    String[][] tableList(String recherche) {
        return bd.read("personne")
                .where("contactPers", "").like(recherche.trim())
                .or("nom", "").like(recherche.trim())
                .or("prenom", "").like(recherche.trim())
                .or("adresse", "").like(recherche.trim())
                .execute();
    }

   

