    public void remplirProprio() {
        String[][] tab = bd.read("personne").execute();
        for (int i = 0; i < tab.length; i++) {
            select.addItem(tab[i][0]);
        }
    }

    public void affiche() {
        System.out.println(id);
        try {
            ResultSet rs = bd.read("voiture").where("matricule ", "= ?").getResultSet(new String[]{id});
            if (rs.next()) {
                matr.setText(rs.getString("matricule"));
                marque.setText(rs.getString("marque"));
                color.setText(rs.getString("couleur"));
                select.addItem(rs.getString("contactPers"));

            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

