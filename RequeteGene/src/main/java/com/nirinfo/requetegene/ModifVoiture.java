    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        // TODO add your handling code here:
        bd.delete("voiture").where("contactPers", "= ?").values(new String[]{id});
        v.affiche();
        this.dispose();
    }//GEN-LAST:event_deleteMouseClicked

    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        // TODO add your handling code here:
        String matricule = matr.getText().trim();
        String marq = marque.getText().trim();
        String couleur = color.getText().trim();
        String contactPers = (String) select.getSelectedItem();
        if (matr.getText().isBlank() || marque.getText().isEmpty() || color.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Remplir tout les champ !!! ");
            return;
        }
        bd.update("voiture").set(new String[]{"matricule", "marque", "couleur", "contactPers"}).where("matricule", "= ?")
                .values(new String[]{matricule, marq, couleur, contactPers, id});
        JOptionPane.showMessageDialog(this, "Utilisateur Modifier");

        matr.setText("");
        marque.setText("");
        color.setText("");

        v.affiche();
        this.dispose();
    }//GEN-LAST:event_saveMouseClicked

