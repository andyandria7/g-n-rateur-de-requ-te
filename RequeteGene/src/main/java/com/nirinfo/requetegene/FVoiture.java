    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
        // TODO add your handling code here:
        String contactPers = (String) select.getSelectedItem();
        if (matr.getText().isBlank() || marque.getText().isEmpty() || color.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Remplir tout les champ !!! ");
            return;
        }
        this.db.insert("voiture")
                .fields(new String[]{"matricule", "marque", "couleur", "contactPers"})
                .values(new String[]{matr.getText(), marque.getText(), color.getText(), contactPers});
        JOptionPane.showMessageDialog(this, "Voiture Ajouter");

        matr.setText("");
        marque.setText("");
        color.setText("");

        this.affiche();

    }//GEN-LAST:event_SaveMouseClicked

